// Side navigation
			function w3_open() {
			    var x = document.getElementById("mySidebar");
			    x.style.width = "30%";
			    x.style.fontSize = "30px";
			    x.style.paddingTop = "2%";
			    x.style.display = "block";
			}
			function w3_close() {
			    document.getElementById("mySidebar").style.display = "none";
			}
